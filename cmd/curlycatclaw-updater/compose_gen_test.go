func TestGenerateOverlayVolumeDedup(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "overlay.yml")

	now := time.Now()
	services := []*ServiceSpec{
		{
			Name:    "svc-a",
			Image:   "img:1",
			Volumes: map[string]string{"shared-vol": "/data/a"},
			AddedAt: now,
		},
		{
			Name:    "svc-b",
			Image:   "img:2",
			Volumes: map[string]string{"shared-vol": "/data/b", "extra-vol": "/extra"},
			AddedAt: now.Add(time.Second),
		},
	}

	if err := GenerateOverlay(services, path); err != nil {
		t.Fatalf("GenerateOverlay: %v", err)
	}

	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("ReadFile: %v", err)
	}

	content := string(data)

	// Count occurrences of "shared-vol:" in the top-level volumes section.
	// The volumes section should have exactly one entry for shared-vol.
	volumeSection := content[strings.LastIndex(content, "\nvolumes:\n"):]
	sharedCount := strings.Count(volumeSection, "shared-vol:")
	if sharedCount != 1 {
		t.Errorf("shared-vol appears %d times in volumes section, want 1; section:\n%s", sharedCount, volumeSection)
	}

	// extra-vol should also appear exactly once.
	extraCount := strings.Count(volumeSection, "extra-vol:")
	if extraCount != 1 {
		t.Errorf("extra-vol appears %d times in volumes section, want 1", extraCount)
	}
}


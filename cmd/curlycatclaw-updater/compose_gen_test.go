func TestGenerateOverlaySingleService(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "overlay.yml")

	services := []*ServiceSpec{
		{
			Name:  "redis",
			Image: "redis:7-alpine",
			Ports: map[string]string{"6379": "6379"},
			Volumes: map[string]string{
				"redis-data": "/data",
			},
			Env: map[string]string{
				"REDIS_ARGS": "--maxmemory 256mb",
			},
			Healthcheck: &Healthcheck{
				Test:     []string{"CMD", "redis-cli", "ping"},
				Interval: "15s",
				Timeout:  "3s",
				Retries:  5,
			},
			AddedAt: time.Now(),
		},
	}

	if err := GenerateOverlay(services, path); err != nil {
		t.Fatalf("GenerateOverlay: %v", err)
	}

	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("ReadFile: %v", err)
	}

	content := string(data)

	// Verify header.
	if !strings.Contains(content, "Auto-generated by curlycatclaw-updater") {
		t.Error("missing header comment")
	}

	// Verify service block.
	if !strings.Contains(content, "  redis:") {
		t.Error("missing service name")
	}
	if !strings.Contains(content, "    image: redis:7-alpine") {
		t.Error("missing image line")
	}

	// Verify ports.
	if !strings.Contains(content, `      - "6379:6379"`) {
		t.Error("missing port mapping")
	}

	// Verify volumes.
	if !strings.Contains(content, "      - redis-data:/data") {
		t.Error("missing volume mapping")
	}

	// Verify environment (value with spaces should be quoted).
	if !strings.Contains(content, `      REDIS_ARGS: "--maxmemory 256mb"`) {
		t.Errorf("missing or incorrect environment line, got:\n%s", content)
	}

	// Verify healthcheck.
	if !strings.Contains(content, `      test: ["CMD", "redis-cli", "ping"]`) {
		t.Error("missing healthcheck test")
	}
	if !strings.Contains(content, "      interval: 15s") {
		t.Error("missing healthcheck interval")
	}
	if !strings.Contains(content, "      timeout: 3s") {
		t.Error("missing healthcheck timeout")
	}
	if !strings.Contains(content, "      retries: 5") {
		t.Error("missing healthcheck retries")
	}

	// Verify restart and networks.
	if !strings.Contains(content, "    restart: unless-stopped") {
		t.Error("missing restart policy")
	}
	if !strings.Contains(content, "      - default") {
		t.Error("missing default network")
	}

	// Verify top-level volumes.
	if !strings.Contains(content, "\nvolumes:\n  redis-data:\n") {
		t.Errorf("missing top-level volumes section, got:\n%s", content)
	}
}

